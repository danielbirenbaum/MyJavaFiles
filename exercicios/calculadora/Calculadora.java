package calculadora;


public class Calculadora {
    public double soma(double x, double y) {
        return x + y;
    }
    public double subtração(double x, double y){
        return x - y;
    }
    public double multiplicacao(double x ,double y) {
        return x * y;
    }
    public double resto(double x, double y) {
        return x % y;
    }
}
